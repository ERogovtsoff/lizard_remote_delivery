// Универсальный компонент: tooltip с пояснением, всплывает при тапе/hover.
// Использование в HTML: <span class="hb-tip" data-tip="Текст подсказки">ⓘ</span>
// Сам JS обработчик ниже — вешает делегированно на document.

let activeTip = null;

function closeActiveTip() {
  if (activeTip) {
    activeTip.remove();
    activeTip = null;
  }
}

function showTip(target) {
  closeActiveTip();
  const text = target.getAttribute('data-tip');
  if (!text) return;
  const tip = document.createElement('div');
  tip.className = 'hb-tip-pop';
  tip.textContent = text;
  document.body.appendChild(tip);

  // Позиционирование под целью
  const rect = target.getBoundingClientRect();
  const tipRect = tip.getBoundingClientRect();
  let left = rect.left + rect.width / 2 - tipRect.width / 2;
  let top = rect.bottom + 8;
  // Корректировка если выходит за границы экрана
  const margin = 8;
  if (left < margin) left = margin;
  if (left + tipRect.width > window.innerWidth - margin) {
    left = window.innerWidth - tipRect.width - margin;
  }
  if (top + tipRect.height > window.innerHeight - margin) {
    top = rect.top - tipRect.height - 8; // показываем сверху
    tip.classList.add('hb-tip-top');
  }
  tip.style.left = left + 'px';
  tip.style.top  = top + 'px';
  activeTip = tip;
}

// Делегированный обработчик
document.addEventListener('click', (e) => {
  const target = e.target.closest('.hb-tip');
  if (target) {
    e.stopPropagation();
    if (activeTip) {
      closeActiveTip();
    } else {
      showTip(target);
    }
  } else if (activeTip) {
    closeActiveTip();
  }
});

document.addEventListener('keydown', (e) => {
  if (e.key === 'Escape') closeActiveTip();
});

