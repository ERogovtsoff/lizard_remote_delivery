export function openHelpModal() {
  const old = document.getElementById('helpModal');
  if (old) { old.remove(); return; }

  const modal = document.createElement('div');
  modal.id = 'helpModal';
  modal.className = 'qp-modal';
  modal.innerHTML = `
    <div class="qp-card help-card">
      <div class="qp-head">
        <span>📖 Как это работает</span>
        <button class="help-close-x" id="helpCloseX" aria-label="Закрыть">✕</button>
      </div>
      <div class="help-search-wrap">
        <input type="text" id="helpSearch" placeholder="🔍 Поиск по справке…" autocomplete="off">
      </div>
      <div class="help-body">
        <nav class="help-nav" id="helpNav"></nav>
        <article class="help-article" id="helpArticle"></article>
      </div>
    </div>`;
  document.body.appendChild(modal);
  modal.onclick = (e) => { if (e.target === modal) modal.remove(); };
  document.getElementById('helpCloseX').onclick = () => modal.remove();

  let currentId = SECTIONS[0].id;
  let filter = '';

  function renderNav() {
    const nav = document.getElementById('helpNav');
    const filtered = filter
      ? SECTIONS.filter(s => {
          const q = filter.toLowerCase();
          return s.title.toLowerCase().includes(q) ||
                 (s.keywords || '').toLowerCase().includes(q) ||
                 s.content.toLowerCase().includes(q);
        })
      : SECTIONS;
    if (!filtered.length) {
      nav.innerHTML = '<div class="help-empty">Ничего не нашлось</div>';
      return;
    }
    nav.innerHTML = filtered.map(s =>
      `<button class="help-nav-item ${s.id === currentId ? 'active' : ''}" data-id="${s.id}">
        <span class="help-nav-ic">${s.icon}</span><span>${escapeHtml(s.title)}</span>
      </button>`
    ).join('');
    nav.querySelectorAll('.help-nav-item').forEach(btn => {
      btn.onclick = () => {
        currentId = btn.getAttribute('data-id');
        renderNav();
        renderArticle();
      };
    });
  }

  function renderArticle() {
    const article = document.getElementById('helpArticle');
    const section = SECTIONS.find(s => s.id === currentId) || SECTIONS[0];
    article.innerHTML = `
      <h2>${section.icon} ${escapeHtml(section.title)}</h2>
      ${section.content}
    `;
    article.scrollTop = 0;
  }

  document.getElementById('helpSearch').oninput = (e) => {
    filter = e.target.value.trim();
    renderNav();
  };

  renderNav();
  renderArticle();
}

